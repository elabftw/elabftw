-- STORAGE UNITS
CREATE TABLE IF NOT EXISTS storage_units (
    id INT unsigned NOT NULL AUTO_INCREMENT,
    name VARCHAR(255),
    parent_id INT unsigned,
    FOREIGN KEY (parent_id) REFERENCES storage_units(id) ON DELETE CASCADE,
    PRIMARY KEY(`id`)
);

